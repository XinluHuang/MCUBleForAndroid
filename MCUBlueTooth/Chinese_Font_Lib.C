//       STC单片机Px口设置
//  -------------+-------------+-----------
//    PxM1[7:0]  |  PxM0[7:0]  |    功能
//  -------------+-------------+-----------
//          0    |      0      |  准双向
//  -------------+-------------+-----------
//          0    |      1      |  推挽输出
//  -------------+-------------+-----------
//          1    |      0      |  高阻输入
//  -------------+-------------+-----------
//          1    |      1      |  开漏输出

void Init_Chinese_Font_Lib(void)
{

	P4M0&=0x74;
	P4M1&=0x74;
    Font_Lib_Chip_CS=1;
	Font_Lib_Chip_SCK = 0;
    Font_Lib_Chip_SO=1;
}

//以串行SPI方式写一个数据字节（命令）至汉字库芯片
void Font_Lib_Chip_Spi_Write(unsigned char Write_Data)
{
	unsigned char i;
	for (i=0;i<8;i++)
	{
		Font_Lib_Chip_SCK=0;
		if ((Write_Data&0x80)!=0)
		{
			Font_Lib_Chip_SI=1;
		}
		else
		{
			Font_Lib_Chip_SI=0;
		}
		Write_Data=(Write_Data<<1);
		Font_Lib_Chip_SCK=1;
	} 
}

//以串行SPI方式从汉字库芯片中读一个数据字节
unsigned char Font_Lib_Chip_Spi_Read(void)
{
	unsigned char i,Read_Data=0;
	Font_Lib_Chip_SO=1;
	for (i=0;i<8;i++)
	{
		Read_Data=(Read_Data<<1);
		Font_Lib_Chip_SCK=0;
		if (Font_Lib_Chip_SO==1)
		{
			Read_Data=Read_Data+1;
		}
		else
		{
			Read_Data=Read_Data+0;
		}
		Font_Lib_Chip_SCK=1;
	} 
	return Read_Data;
}

